package tw.Jackson.ClassTest.OperatorsTest;

public class Class_OperatorsBitwise_Ex4 {

	public static void main(String[] args) {
		int x=1,y=3;
		System.out.println("~"+x+"="+(~x));
		System.out.println(x+"&"+y+"="+(x&y));
		System.out.println(x+"|"+y+"="+(x|y));
		System.out.println(x+"^"+y+"="+(x^y));

	}

}
